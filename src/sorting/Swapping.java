package sorting;

public class Swapping {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int temp;
        temp = num1;
        num1 = num2;
        num2 = temp;
        System.out.println("Num-1: " + num1);
        System.out.println("Num-2: " + num2);

        String st1 = "bitcoin";
        String st2 = "ethereum";
        String tempSt;
        tempSt = st1;
        st1 = st2;
        st2 = tempSt;
        System.out.println("St-1: " + st1);
        System.out.println("St-2: " + st2);

    }
}
